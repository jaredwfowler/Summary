#include "Element.hpp"

//Definition========================================================================================
//==================================================================================================
class Camera : public Element
{
public: //##########################################################################################
   //Constructor(s)=================================================================================
   //===============================================================================================
   Camera(const char* _id, glm::vec3 _eye, glm::vec3 _at, glm::vec3 _up);

   //Destructor=====================================================================================
   //===============================================================================================
   ~Camera(void);

   //Gets===========================================================================================
   //===============================================================================================
   virtual glm::mat4 getViewMatrix(void);

   //Do=============================================================================================
   //===============================================================================================
   virtual bool update(void);

protected: //#######################################################################################
   //Sets===========================================================================================
   //===============================================================================================
   void setUp(glm::vec3 v);
   void setAt(glm::vec3 v);
   void setEye(glm::vec3 v);

private: //#########################################################################################
   //Vars,typedefs,etc.=============================================================================
   //===============================================================================================
   glm::vec3 eye;
   glm::vec3 at;
   glm::vec3 up;


   // There should only need be one instance of each object.

   void operator= (const Camera& m) {}
   Camera(const Camera& m);

};
