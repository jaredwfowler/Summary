//Dependencies======================================================================================
//==================================================================================================
#include "Camera.hpp"

//Method Definitions================================================================================
//==================================================================================================
Camera::Camera(const char* _id, glm::vec3 _eye, glm::vec3 _at, glm::vec3 _up)
   :Element(_id)
{
   this->eye = _eye;
   this->at = _at;
   this->up = _up;
}

//**************************************************************************************************

Camera::~Camera(void)
{
   //Void
}

//**************************************************************************************************

void Camera::setUp(glm::vec3 v)
{
   this->up = v;
}

//**************************************************************************************************

void Camera::setAt(glm::vec3 v)
{
   this->at = v;
}

//**************************************************************************************************

void Camera::setEye(glm::vec3 v)
{
   this->eye = v;
}

//**************************************************************************************************

glm::mat4 Camera::getViewMatrix(void)
{
   return glm::lookAt(this->eye, this->at, this->up);
}

//**************************************************************************************************

bool Camera::update(void)
{
   return true;
}
