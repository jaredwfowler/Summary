#include "SimpleDate.hpp"
#include <string>

//Definition========================================================================================
//==================================================================================================
class StockRating
{
public: //##########################################################################################
   //Vars,typedefs,etc.=============================================================================
   //===============================================================================================
   bool operator< (const StockRating& _r) const;

   //Constructor(s)=================================================================================
   //===============================================================================================
   StockRating(SimpleDate _date, float _rating, std::string _rater);

   //Gets===========================================================================================
   //===============================================================================================
   inline SimpleDate getDate(void) const { return this->date; }
   inline float getRating(void) const { return this->rating; }
   inline std::string getRater(void) const { return this->rater; }

private: //#########################################################################################
   //Vars,typedefs,etc.=============================================================================
   //===============================================================================================
   SimpleDate date;
   float rating;
   std::string rater;

   //Sets===========================================================================================
   //===============================================================================================
   inline void setDate(SimpleDate _date){ this->setDate(_date); }
   inline void setRating(float _x){ this->setRating(_x); }
   inline void setRater(std::string _x){ this->setRater(_x); }

};
